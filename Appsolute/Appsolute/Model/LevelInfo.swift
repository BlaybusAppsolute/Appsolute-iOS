struct LevelInfo {
    let displayName: String  // 화면에 표시할 레벨 이름 (예: "Lv.1 꿈틀이 씨앗-1")
    let xp: Int              // 해당 레벨에 필요한 총 경험치
    let imageName: String    // 레벨과 연관된 이미지 이름
    let levelNumber: Int     // 단순 레벨 번호 (1, 2, 3...)
}
